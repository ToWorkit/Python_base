# 使用 in 判断 是否在
print('l' in 'Hello World')

# 格式字符串
print('%s love me' % 'LP')

# 字符串拼接
a = 'Hello '
b = 'World'
c = ''.join([a, b])
print(c)

# String 内置方法

# 统计字符个数
print('Hello'.count('l'))

# 字符串放中间左右补充 共20个*
print('World'.center(20, '*'))

# 以什么为开头 结尾, 严格规范大小写 可加范围
print('Hello'.startswith('H'))
print('Hello'.endswith('lo'))

